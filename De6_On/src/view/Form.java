    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        File file = new File(filename);
        try {
            FileOutputStream fos = new FileOutputStream(file);
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            List<Nguoi> lst = ql.getList();
            oos.writeObject(lst);
            oos.close();
            JOptionPane.showMessageDialog(this, "Ghi thành công");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi vao ra ");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnOpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenFileActionPerformed
        // TODO add your handling code here:
        File file = new File(filename);
        if (file.exists() == false) {
            JOptionPane.showMessageDialog(this, "File không tồn tại");
        }
        try {
            FileInputStream fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            List<Nguoi> lst = (List<Nguoi>) ois.readObject();
            ois.close();
            ql.setList(lst);
            loadTable();
            JOptionPane.showMessageDialog(this, "Đoc thành công");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy file");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi vao ra");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu");
        }
    }//GEN-LAST:event_btnOpenFileActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Nguoi x = getFormData();
        if (x == null) {
            return;
        }
        ql.insert(x);
        loadTable();
        JOptionPane.showMessageDialog(this, "Theem thanh cong");
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        // TODO add your handling code here:
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        String loai = tblTable.getValueAt(row, 0).toString();
        String ma = tblTable.getValueAt(row, 1).toString();
        String ten = tblTable.getValueAt(row, 2).toString();
        String email = tblTable.getValueAt(row, 3).toString();

        cbbLoai.setSelectedItem(loai);
        txtMa.setText(ma);
        txtTen.setText(ten);
        txtEmail.setText(email);

    }//GEN-LAST:event_tblTableMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblTable.getSelectedRow();
        if (row == -1) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Ban muon xoá chứ");
        if (confirm == JOptionPane.NO_OPTION) {
            return;
        }
        ql.delete(row);
        loadTable();
        clearForm();
        JOptionPane.showMessageDialog(this, "Xoá thanh cong");
    }//GEN-LAST:event_btnDeleteActionPerformed

