    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        List<Nguoi> ds = ql.getList();

        model.setRowCount(0);
        for (Nguoi d : ds) {
            Nguoi ng = (Nguoi) d;
            Object[] row = {
                ng.getLoai(),
                ng.getMaKH(),
                ng.getTenKH(),
                ng.getEmail()
            };
            model.addRow(row);
        }
    }

    private void clearForm() {
        txtMa.setText("");
        txtTen.setText("");
        txtEmail.setText("");
        cbbLoai.setSelectedIndex(0);
    }

    private Nguoi getFormData() {
        String loai = cbbLoai.getSelectedItem().toString();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String email = txtEmail.getText();

        if (ma.trim().length() == 0 || ten.trim().length() == 0 || email.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Khoong duoc bo trong");
            return null;
        }
        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Email phải có kí tự @");
            return null;
        }

        Nguoi n = new Nguoi(loai, Integer.parseInt(ma), ten, email);
        return n;
    }

