    @Override
    public void run() {
        int gio = 0, phut = 0, giay = 0;
        while (true) {
            giay++;
            if (giay == 60) {
                phut++;
                giay = 0;
            }
            if (phut == 60) {
                gio++;
            }
            String tg = gio + ":" + phut + ":" + giay;
            lbtClock.setText(tg);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }
