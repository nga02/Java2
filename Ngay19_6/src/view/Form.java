    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        List<SinhVien> sv = ql.getList();
        model.setRowCount(0);
        for (SinhVien d : sv) {
            Object[] row = {
                d.getMaSV(),
                d.getTenSV(),
                d.getNgaySing(),
                d.getGioiTinh() == 1 ? "Nam" : "Nữ",
                d.getTuoi(),
                d.getMonHoc(),
                d.getHocPhi(),
                d.getEmail()
            };
            model.addRow(row);
        }
    }

