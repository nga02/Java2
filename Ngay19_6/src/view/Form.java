    private SinhVien getFormData() {
        String maSV = txtMa.getText();
        String tenSV = txtName.getText();;
        String ngaySinh = txtDate.getText();;
        int gt = this.rdoNam.isSelected() == true ? 1 : 0;
        String tuoi = txtTuoi.getText();;
        String monHoc = cbbMonHoc.getSelectedItem().toString();
        String hocPhi = txtPhi.getText();;
        String email = txtEmail.getText();;

        if (maSV.trim().length() == 0 || tenSV.trim().length() == 0 || ngaySinh.trim().length() == 0
                || tuoi.trim().length() == 0
                || hocPhi.trim().length() == 0 || email.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Khoong duoc bo trong");
            return null;
        } else if (!checkSo(tuoi)) {
            JOptionPane.showMessageDialog(this, "Sai dinhj dang");
            return null;
        } else if (Integer.parseInt(tuoi) <= 0) {
            JOptionPane.showMessageDialog(this, "Phai laf so nguyen duong");
            return null;
        } else if (!checkChuoi(tenSV)) {
            JOptionPane.showMessageDialog(this, "Họ tên ko gồm cả số");
            return null;
        } else if (Double.parseDouble(hocPhi) < 0) {
            JOptionPane.showMessageDialog(this, "Luongw pAI lon hon 0");
            return null;
        } else if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Email phair cos chua @");
            return null;
        }

        SimpleDateFormat sdf = new SimpleDateFormat();
        sdf.applyPattern("dd/mm/yyyy");
        try {
            Date date = sdf.parse(txtDate.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sai định dạng ngày sinh");
            return null;
        }

        SinhVien sv = new SinhVien(Long.parseLong(maSV), tenSV, ngaySinh, gt, Integer.parseInt(tuoi), monHoc, Double.parseDouble(hocPhi), email);
        return sv;
    }

    private boolean checkSo(String text) {
        Pattern pattern = Pattern.compile("\\d+");
        Matcher matcher = pattern.matcher(text);
        return matcher.matches();//"\\d+"
    }

    private boolean checkChuoi(String text) {
        Pattern pattern = Pattern.compile("[a-zA-Z ]+$");
        Matcher matcher = pattern.matcher(text);
        return matcher.matches();//"\\d+"
    }

